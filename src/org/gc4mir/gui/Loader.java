    public void checkHelp(){
        Object[] options = {"Yes", "No"};
        int n = JOptionPane.showOptionDialog(this,
        "Welcome to EasyClassifier!\n\n"
                + "Would you like to turn on help? \n"+
        "(this option can be changed anytime during execution)",
        "Hello!",
        JOptionPane.YES_NO_CANCEL_OPTION,
        JOptionPane.QUESTION_MESSAGE,
        null,
        options,
        options[1]);
        
        if(n == 0){
            core.setHelp(true);
        }else{
            core.setHelp(false);
        }
    }
    
